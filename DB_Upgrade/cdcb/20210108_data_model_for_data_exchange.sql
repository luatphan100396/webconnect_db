CREATE TABLE DATA_EXCHANGE_REQUEST_TABLE
 (
 REQUEST_KEY INT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
 USER_KEY INT,
 REQUEST_NAME varchar(300) not null,
 INPUT_PATH varchar(3000),
 REQUEST_DETAIL XML not null,
 STATUS varchar(10) not null,
 IP_ADDRESS varchar(50),
 CREATED_TIME TIMESTAMP not null,
 START_TIME TIMESTAMP,
 END_TIME TIMESTAMP,
 CANCEL_BY varchar(30) 
 );
  
 
  CREATE TABLE DATA_EXCHANGE_OPERATION_TABLE
 (
 OPERATION_KEY INT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
 REQUEST_KEY INT,
 OPERATION_NAME varchar(300),
 OUTPUT_PATH varchar(3000),
 STATUS varchar(10), 
 CREATED_TIME TIMESTAMP,
 START_TIME TIMESTAMP,
 END_TIME TIMESTAMP,
 CANCEL_BY varchar(30) 
 );
 