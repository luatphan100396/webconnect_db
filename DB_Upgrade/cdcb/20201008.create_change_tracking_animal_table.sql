CREATE TABLE CHANGE_TRACKING_ANIMAL
(
	ID				INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,	
	ANIMAL_ID		CHAR(17),
	ANIM_KEY		INT,
	SPECIES_CODE	CHAR(1),
	SEX_CODE		CHAR(1),
	
	PREFERRED_ID	CHAR(17),
	DETAIL_XML	XML,
	STATUS			VARCHAR(11),
	CREATE_TIME		TIMESTAMP
	
);

CREATE INDEX NIX_CHANGE_TRACKING_ANIMAL_ANIMAL_ID
ON CHANGE_TRACKING_ANIMAL (ANIMAL_ID) ALLOW REVERSE SCANS;

CREATE INDEX NIX_CHANGE_TRACKING_ANIMAL_ANIM_KEY
ON CHANGE_TRACKING_ANIMAL (ANIM_KEY) ALLOW REVERSE SCANS;

CREATE INDEX NIX_CHANGE_TRACKING_ANIMAL_SPECIES_CODE
ON CHANGE_TRACKING_ANIMAL (SPECIES_CODE) ALLOW REVERSE SCANS;

CREATE INDEX NIX_CHANGE_TRACKING_ANIMAL_SEX_CODE
ON CHANGE_TRACKING_ANIMAL (SEX_CODE) ALLOW REVERSE SCANS;